# Код простой текстовой боевой игры, где игрок и компьютер управляют героями с различными характеристиками.
# Импортируем модуль random для генерации случайного урона при атаке
import random

# Класс Hero представляет героя в игре (игрока или компьютера)
class Hero:
    def __init__(self, name):
        # Имя героя, задаваемое пользователем или по умолчанию "Компьютер"
        self.name = name
        # Здоровье героя, изначально равно 100
        self.health = 100
        # Сила атаки героя, изначально равна 20
        self.attack_power = 20

    # Метод attack позволяет герою атаковать другого героя (other)
    def attack(self, other):
        # Генерируем случайный урон от 1 до силы атаки героя
        damage = random.randint(1, self.attack_power)
        # Выводим информацию о том, кто кого атаковал и сколько нанесён урона
        print(f"{self.name} атакует {other.name} и наносит {damage} урона.")
        # Уменьшаем здоровье противника на величину нанесённого урона
        other.health -= damage
        # Сообщаем, сколько здоровья осталось у противника после атаки
        print(f"У {other.name} осталось {other.health} здоровья.\n")

    # Метод is_alive проверяет, жив ли герой (осталось ли у него здоровье)
    def is_alive(self):
        # Если здоровье больше 0 — герой жив, иначе — мёртв
        return self.health > 0

# Класс Game управляет самой игрой: созданием персонажей, началом и процессом боя
class Game:
    def __init__(self, player_name):
        # Создаём героя-игрока с именем, введённым пользователем
        self.player = Hero(player_name)
        # Создаём героя-компьютера с именем "Компьютер"
        self.computer = Hero("Компьютер")

    # Метод start запускает игру и управляет ходами игроков
    def start(self):
        # Приветствуем игрока и сообщаем начало игры
        print("Игра началась!\n")
        # Определяем, чей сейчас ход: 0 - игрок, 1 - компьютер
        turn = 0  # Начинает игрок

        # Цикл продолжается, пока оба героя живы
        while self.player.is_alive() and self.computer.is_alive():
            if turn == 0:
                # Ход игрока: он атакует компьютера
                self.player.attack(self.computer)
                # Передаём ход компьютеру
                turn = 1
            else:
                # Ход компьютера: он атакует игрока
                self.computer.attack(self.player)
                # Передаём ход обратно игроку
                turn = 0

        # Проверяем, кто выжил — победитель определён
        if self.player.is_alive():
            # Игрок победил
            print(f"{self.player.name} победил!")
        else:
            # Компьютер победил
            print(f"{self.computer.name} победил!")

# Эта часть выполняется только если файл запущен как основной скрипт
if __name__ == "__main__":
    # Запрашиваем имя игрока
    player_name = input("Введите имя вашего героя: ")
    # Создаём экземпляр игры с этим именем
    game = Game(player_name)
    # Запускаем игру
    game.start()
